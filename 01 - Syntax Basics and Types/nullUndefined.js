
//   Null and Undefined Data Types:

//   Null vs Undefined 

//   Both reperesent a complete lack of value; however the only difference is: 
//   Null is an intentional lack of Value, 
//   Undefined is  an unintetntional lack of Value


let age;                     // If the variable age has no value, a value of UNDEFINED is assigned by the browser
console.log(age,age+3, `the age is ${age}`)

let age1 = null;             // Null is a value we intentionally and explicitly assign to have a DEFINED value of NULL.
console.log(age1, age1+3, `the age is ${age1}`)


