/*     Math Object:

JavaScript has many built in Options, and the ' Math ' Object is one of them 
 -> Capital M -->  Math */
console.log(Math);  // View the 'Math Object' in the DevTools Console, notice all the methods and properties in the available in the Math object 

// Mathematical Constants: USE CAPS!
console.log(Math.PI);   // .PI There are many Mathematical Constants avaialable in in the Math Object
console.log(Math.E);    // The mathematical constant e. This is Euler's number, the base of natural logarithms.

const area = 7.7 ;    //  Create a variable called area, with a value of 7.7


// Mathmatical Methods
console.log(Math.round(area));    // The .round method of the Math Object, 
// ROUNDS UP to the nearest integer

console.log(Math.floor(area));    // The .floor method of the Math Object, 
// FLOORS the area parameter to the nearest integer. Rounds DOWN to neaest interger.

console.log(Math.ceil(area));    // The .ceil method, does exactly OPPOSITE of floor, 
// Ceiling - Rounds UP

console.log(Math.trunc(area));    // The .trunc method, truncates the decimal to an integer
// truncates the decimal to an integer

